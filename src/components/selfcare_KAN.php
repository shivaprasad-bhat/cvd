                <article class="card-title c-title">
                    ಹೃದ್ರೋಗಿಗಳಲ್ಲಿ ಮೂತ್ರಪಿಂಡದ ಕಾರ್ಯ ಪರೀಕ್ಷೆ (RFT) ಮತ್ತು ಲಿವರ್ ಕಾರ್ಯ ಪರೀಕ್ಷೆ (LFT) ಯನ್ನು ಪರಿಶೀಲಿಸುವ ಪ್ರಾಮುಖ್ಯತೆ ಏನು?
                </article>
                <p>
                    • ಹೃದಯ ರಕ್ತನಾಳೀಯ ಕಾಯಿಲೆ ಹೊಂದಿರುವ ರೋಗಿಗಳು ಹೆಚ್ಚಾಗಿ ಮೂತ್ರಪಿಂಡ ಮತ್ತು ಯಕೃತ್ತಿನ ಹಾನಿಗೆ ಒಳಗಾಗುತ್ತಾರೆ.<br>
                    • ಆದ್ದರಿಂದ RFT & LFT ಇವು ಸರಳ ರಕ್ತ ಪರೀಕ್ಷೆಯಾಗಿದ್ದು ಅದು ನಿಮ್ಮ ಮೂತ್ರಪಿಂಡಗಳು, ಯಕೃತ್ತಿನ ಸಮಸ್ಯೆಗಳನ್ನು ಗುರುತಿಸುತ್ತದೆ ಮತ್ತು ಈ ಪರಿಸ್ಥಿತಿಗಳನ್ನು ಮೇಲ್ವಿಚಾರಣೆ ಮಾಡಲು ವೈದ್ಯರಿಗೆ ಸಹಾಯ ಮಾಡುತ್ತದೆ.<br>

                </p>
            </article>
        </div>
    </section>
</section>

