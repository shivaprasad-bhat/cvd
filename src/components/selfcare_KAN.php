                <p>ಕಣ್ಣುಗಳು ನಿಮ್ಮ ಹೃದಯದ ಆರೋಗ್ಯದ ಬಗ್ಗೆ ತಿಳಿಸುತ್ತದೆ. ಹೆಚ್ಚಿನ ರಕ್ತದೊತ್ತಡ, ಕೊಲೆಸ್ಟ್ರಾಲ್, ಇವು ಹೃದಯ ಕಾಯಿಲೆಗೆ ಮುಖ್ಯಅಂಶವಾಗಿದ್ದು ಹೃದಯಾಘಾತದ ಅಪಾಯವನ್ನು ಹೆಚ್ಚಿಸುತ್ತದೆ.<br>
                    • ನೀವು ಅಧಿಕ ಕೊಲೆಸ್ಟ್ರಾಲ್ ಅಥವಾ ಅಧಿಕ ರಕ್ತದೊತ್ತಡವನ್ನು ಹೊಂದಿದ್ದರೆ, ಇದು ಕಣ್ಣುಗಳ ಮುಖ್ಯ ರಕ್ತ ಪೂರೈಕೆಯನ್ನು ಹಾನಿಗೊಳಿಸುತ್ತದೆ.
