                <article class="card-title c-title">
                    ಔಷಧಿ ಮತ್ತು ಮರು ಭೇಟಿ:
                </article>
                <p>
                    • ನಿಮ್ಮ ಔಷಧಿಗಳನ್ನು ಬಿಟ್ಟುಬಿಡದೇ ಸರಿಯಾದ ಸಮಯದಲ್ಲಿ, ಯಾವುದೇ ಗೊಂದಲವಿಲ್ಲದೆ ತೆಗೆದುಕೊಳ್ಳಿ.<br>
                    • ಔಷಧಿಗಳನ್ನು, ಯಾವುದೇ ಕಾರಣಕ್ಕೂ ನೀವು ಪರೀಕ್ಷಿಸುವ ವೈದ್ಯರನ್ನು ಸಂಪರ್ಕಿಸದೆ ನಿಲ್ಲಿಸಬೇಡಿ.<br>
                    • ಔಷಧಿಯು ಹೃದಯಾಘಾತ, ಹೃದಯ ಸ್ತಂಭನ, ಪಾರ್ಶ್ವವಾಯು ಅಥವಾ ಹೃದಯ ವೈಫಲ್ಯದ ಅಪಾಯವನ್ನು ನಿಮ್ಮಲ್ಲಿ ಕಡಿಮೆ ಮಾಡಲು ಸಹಾಯ ಮಾಡುತ್ತದೆ.<br>
                    • ಔಷಧಿಗಳು ಹೆಚ್ಚಿನ ರಕ್ತದೊತ್ತಡ, ರಕ್ತದಲ್ಲಿನ ಅಧಿಕ ಕೊಬ್ಬಿನಾಂಶ ನಿಯಂತ್ರಿಸಿ, ನಿಮ್ಮ ಜೀವನದ ಗುಣಮಟ್ಟವನ್ನು ಸುಧಾರಿಸುವ ಮೂಲಕ ರೋಗಲಕ್ಷಣಗಳನ್ನು ಕಡಿಮೆ ಮಾಡಲು ಸಹಾಯ ಮಾಡುತ್ತದೆ.<br>
                </p>
            </article>
        </div>
    </section>
</section>

