                <article class="card-title c-title">
                    ಕೊಲೆಸ್ಟ್ರಾಲ್ ಪರೀಕ್ಷೆಯ ಪರಿಶೀಲಿಸುವ ಪ್ರಾಮುಖ್ಯತೆ ಏನು?
                </article>
                <p>
                    • ಅಸಹಜ ರಕ್ತದ ಕೊಬ್ಬಿನ ಮಟ್ಟಗಳು ಪರಿಧಮನಿಯ ಕಾಯಿಲೆ ಮತ್ತು ಹೃದಯಾಘಾತದ ಅಪಾಯದಲ್ಲಿ ಪರಸ್ಪರ ಸಂಬಂಧವನ್ನು ಹೊಂದಿವೆ.<br>
                    • ಸಾಮಾನ್ಯ ಮಟ್ಟಗಳು ಈ ಕೆಳಗಿನಂತಿರಬೇಕು,<br>
                    Total cholesterol : 140-200 mg/dL<br>
                    Triglycerides : 60-150 mg/dL<br>
                    HDL cholesterol : 40-60 mg/dL<br>
                    LDL cholesterol : 50-130 mg/dL<br>
                    Total cholesterol/HDL cholesterol ratio : Less than 5 (5 ಕ್ಕಿಂತ ಕಡಿಮೆ)<br>

                </p>
            </article>
        </div>
    </section>
</section>

