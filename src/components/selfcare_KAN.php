                    ಗ್ಲೈಕೊ ಎಚ್ಬಿ ಪರಿಶೀಲಿಸುವ ಪ್ರಾಮುಖ್ಯತೆ ಏನು?
                </article>
                <p>
                    • ಇದು ಪರೀಕ್ಷೆಯ 2-3 ತಿಂಗಳ ಮೊದಲು, ವ್ಯಕ್ತಿಯ ಸರಾಸರಿ ರಕ್ತದಲ್ಲಿನ ಗ್ಲೂಕೋಸ್ ಮಟ್ಟ ಏನೆಂದು ತೋರಿಸುತ್ತದೆ. ಕಾಲಾನಂತರದಲ್ಲಿ ವ್ಯಕ್ತಿಯ ಮಧುಮೇಹವನ್ನು ಎಷ್ಟು ಚೆನ್ನಾಗಿ ನಿಯಂತ್ರಿಸಲಾಗುತ್ತದೆ ಎಂಬುದನ್ನು ನಿರ್ಧರಿಸಲು ಸಹ ಇದು ಸಹಾಯ ಮಾಡುತ್ತದೆ. <br>
                    • ಗ್ಲೈಕೊ ಎಚ್‌ಬಿಯಲ್ಲಿನ ಪ್ರತಿ 1% ಹೆಚ್ಚಳವು 15-18% ಹೃದಯ ರಕ್ತನಾಳದ ರೋಗಗಳಿಗೆ ಅಪಾಯವನ್ನು ಹೊಂದಿರುತ್ತದೆ<br>

                    5.7% ಕ್ಕಿಂತ ಕಡಿಮೆ - ಸಾಮಾನ್ಯ <br>
                    5.7 - 6.4% - ಮಧುಮೇಹ ಪಡೆಯುವ ಹೆಚ್ಚಿನ ಅವಕಾಶ <br>
                    6.5% ಅಥವಾ ಅಧಿಕ– ಮಧುಮೇಹ<br>
                    Fasting Blood Sugar (FBS) : 70-100 mg/dL (ಖಾಲಿ ಹೊಟ್ಟೆಯಲ್ಲಿ ಮಾಡುವ ರಕ್ತ ಪರೀಕ್ಷೆ)<br>
                </p>
            </article>
